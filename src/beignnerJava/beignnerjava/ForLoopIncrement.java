
package beignnerJava.beignnerjava;


public class ForLoopIncrement {
    public static void main(String[] args) {
        for(int i=1;i<=10;i++){
        System.out.println("SHEBLEY MSI");
    }
        for(int i=1;i<=10;i++){
        System.out.println(i+" SHEBLEY MSI");
    }
        for(int i=1;i<=10;i++){
        System.out.println(i);
    }
        for(int i=2;i<=10;i=i+2){
        System.out.println(i);
    }
        for(int i=1;i<=10;i=i+2){
        System.out.println(i);
    }
        for(int i=10;i>=1;i--){
        System.out.println(i);
    }
    
  }
}