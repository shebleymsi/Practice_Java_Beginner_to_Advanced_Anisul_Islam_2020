
package dataType;

public class dataType {


    public static void main(String[] args) {
        
        boolean b = true; //Dynamic initilization.
        char c = 'a';
        byte by = 100;
        short s = 10000;
        int i = 100000;
        long l = 10000000;
        float f = 10.0000f;
        double d = 12.02;
        
        System.out.printf("boolean b = %b\n",b);
        System.out.printf("character c = %c\n",c);
        System.out.printf("byte by = %d\n",by);
        System.out.printf("short s = %d\n",s);
        System.out.printf("intiger i = %d\n",i);
        System.out.printf("long l = %d\n",l);
        System.out.printf("float f = %.2f\n",f);
        System.out.printf("double d = %.3f\n",d);
        
        }
    
}
